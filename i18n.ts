const DICTS: Record<Lang, Record<string, string>> = { en, fr, de, es, it, pt };

// Export UI for backward compatibility if any legacy components import it locally (though they shouldn't with t())
export const UI = DICTS;

export function safeLang(input: string | null | undefined): Lang {
  const v = (input || "").toLowerCase();
  if (v === "en" || v === "fr" || v === "de" || v === "es" || v === "it" || v === "pt") return v as Lang;
  return "en";
}

export function t(lang: Lang, key: string): string {
  // Hard fallback to English to prevent blank UI.
  return DICTS[lang]?.[key] ?? DICTS.en[key] ?? key;
}
